package segunda_aula;

public class MediaAritmetica {

    public static void main(String[] args) {

        float media1, media2, resultado;

        media1 = (8 + 9 + 7) /3;
        media2 = (4 + 5 + 6) /3;
        resultado = (media1 + media2) / 2;

        System.out.println("A média aritmética do primeiro é: " + media1);
        System.out.println("A média aritmética do segundo é: " + media2);
        System.out.println("A média das medias é: " + resultado);
    }
}
